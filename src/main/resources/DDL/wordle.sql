CREATE TABLE WORDLE_WORDS
(
    ID               NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FULL_TEXT        VARCHAR2(255),
    CHARACTERS       VARCHAR2(255),
    CHARACTER_LENGTH NUMBER,
    CAN_ANSWER       NUMBER(1),
    CREATED_AT       TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT       TIMESTAMP DEFAULT SYSTIMESTAMP,
    DELETED_AT       TIMESTAMP DEFAULT NULL
);

CREATE TABLE WORDLE_QUIZZES
(
    ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    WORD_ID     NUMBER,
    STARTED_AT  TIMESTAMP,
    FINISHED_AT TIMESTAMP,
    CREATED_AT  TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT  TIMESTAMP DEFAULT SYSTIMESTAMP,
    DELETED_AT  TIMESTAMP DEFAULT NULL
);

CREATE TABLE WORDLE_TRIALS
(
    ID         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    QUIZ_ID    NUMBER,
    UUID       NUMBER,
    CHARACTERS VARCHAR2(255), -- ㅇ,ㅣ,ㄴ,ㅅ,ㅡ,ㅌ,ㅏ 로 표시
    STRIKES    VARCHAR2(255), -- 1, 0, 0, 1,0,0,0 으로 표시
    BALLS      VARCHAR2(255), -- 1, 0, 0, 1,0,0,0 으로 표시
    NONES      VARCHAR2(255), -- 1, 0, 0, 1,0,0,0 으로 표시
    IS_ANSWER  NUMBER(1),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL
);